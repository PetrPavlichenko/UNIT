#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		n = (n * (-1));
		ft_putchar('-');
	}
	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (n >= 10)
		{
			ft_putnbr_fd((n / 10), fd);
			ft_putnbr_fd((n % 10), fd);
		}
		else
			ft_putchar(n + '0');
	}
}
